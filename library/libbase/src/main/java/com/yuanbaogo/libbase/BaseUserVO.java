package com.yuanbaogo.libbase;

/**
 * 基本用户签证官
 *
 * @author 肖立彤
 * @why
 * @how
 * @date 2021/08/31
 */

public class BaseUserVO {

    private String ybCode;


    private String nickName;


    private String portraitUrl;


    private String backgroundPictureUrl;


    private String signature;


    private String phone;


    private String name;


    private String gender;


    private String age;


    public String getYbCode() {
        return ybCode;
    }

    public void setYbCode(String ybCode) {
        this.ybCode = ybCode;
    }

    public String getNickName() {
        return nickName;
    }

    public void setNickName(String nickName) {
        this.nickName = nickName;
    }

    public String getPortraitUrl() {
        return portraitUrl;
    }

    public void setPortraitUrl(String portraitUrl) {
        this.portraitUrl = portraitUrl;
    }

    public String getBackgroundPictureUrl() {
        return backgroundPictureUrl;
    }

    public void setBackgroundPictureUrl(String backgroundPictureUrl) {
        this.backgroundPictureUrl = backgroundPictureUrl;
    }

    public String getSignature() {
        return signature;
    }

    public void setSignature(String signature) {
        this.signature = signature;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }
}
